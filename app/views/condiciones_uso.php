<h1>Aviso legal</h1>

<h2>Contenido</h2>

<p>El presente sitio web tiene como objetivo facilitar la transferencia de
ficheros entre los usuarios de la Universidad de Sevilla.</p>

<h2>Condiciones de acceso y uso</h2>

<p>Las condiciones de acceso y utilización de este sitio web se rigen conforme
a la normativa vigente y, en concreto, por la Ley Orgánica 15/1999, de 13 de
diciembre, de Protección de Datos de Carácter Personal, la Ley 34/2002, de
11 de julio, de Servicios de la Sociedad de la información y de Comercio
Electrónico. El identificador de usuario, que se corresponde con el UVUS, y
su contraseña, tienen carácter personal e intransferible, siendo
responsabilidad del usuario su confidencialidad y correcto uso.</p>

<p>Los usuarios deben hacer un uso correcto y leal de este sitio web, debiendo
abstenerse de realizar cualesquiera tipo de acciones que puedan ocasionar
daño o perjuicio a la Universidad de Sevilla, bien en su imagen, bien en los
elementos que soportan el sitio web, entendiendo por tales los ficheros que
se comparten en el mismo y que deben ser de titularidad del que los
proporciona o tener el permiso adecuado para hacerlo. </p>
